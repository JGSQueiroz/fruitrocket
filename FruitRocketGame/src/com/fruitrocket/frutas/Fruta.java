package com.fruitrocket.frutas;

public class Fruta {
	
	public int tipo;
	public int alturaFruta;
	public int larguraFruta;
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	public int getAlturaFruta() {
		return alturaFruta;
	}
	public void setAlturaFruta(int alturaFruta) {
		this.alturaFruta = alturaFruta;
	}
	public int getLarguraFruta() {
		return larguraFruta;
	}
	public void setLarguraFruta(int larguraFruta) {
		this.larguraFruta = larguraFruta;
	}
	
	

}
